
	package day4;
	import java.util.Scanner;
	
	public class ForLoop {
		
		public static void main(String [] args) {
			Scanner na = new Scanner(System.in);
			int table=na.nextInt();
			for (int i=1;i<=10;i++) {
				System.out.println(table+"x"+i+"="+i*table);
				
			}
			
			
			
		}
		}

	